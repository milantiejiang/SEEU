public class IMG {
	private String IMG_ID;//IMG id
	private String IMG_PATH;//地址
    private int IMG_WITH;//宽
    private int IMG_HIGH;//高
	public String getIMG_ID() {
		return IMG_ID;
	}
	public void setIMG_ID(String iMG_ID) {
		IMG_ID = iMG_ID;
	}
	public String getIMG_PATH() {
		return IMG_PATH;
	}
	public void setIMG_PATH(String iMG_PATH) {
		IMG_PATH = iMG_PATH;
	}
	public int getIMG_WITH() {
		return IMG_WITH;
	}
	public void setIMG_WITH(int iMG_WITH) {
		IMG_WITH = iMG_WITH;
	}
	public int getIMG_HIGH() {
		return IMG_HIGH;
	}
	public void setIMG_HIGH(int iMG_HIGH) {
		IMG_HIGH = iMG_HIGH;
	}
	
    
    
}
